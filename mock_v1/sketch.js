const BLACK = "#000"
const GRAY = "#222"
const WHITE = "#FFF"
const MAIN_COLOR = "#07F"

function setup() {
  resolution = 720 // max resolution for Twitter: 720
  frameWidth = 0
  pixelSize = (resolution - 2 * frameWidth) / (numDisplayPitch + 4)
  cornerRadius = pixelSize / 2

  createCanvas(resolution, resolution)

  stroke(BLACK)
  strokeCap(SQUARE) // ROUND, SQUARE, PROJECT
  strokeJoin(BEVEL) // MITER, BEVEL, ROUND
  strokeWeight(pixelSize / 2)
  rectMode(CORNERS) // CENTER, CORNER

  frameRate(renderingFrameRate)
}

function draw() {
  background(GRAY)

  if (frameCount > 60 * maxMidiNotesTime * videoFrameRate / (division * bpm)) {
    noLoop()
  }

  stroke(BLACK)
  const dv = (1 / 60) * division * bpm / videoFrameRate
  for (let midiNote of midiNotes) {
    coordinateTopLeft = {
      x: resolution / 2 + pixelSize * (midiNote.startTime - dv * frameCount) / 4,
      y: pixelSize * (maxMidiNoteNumber - midiNote.noteNumber + 1) + frameWidth - pixelSize / 2
    }
    coordinateBottomRight = {
      x: resolution / 2 + pixelSize * (midiNote.endTime - dv * frameCount) / 4,
      y: pixelSize * (maxMidiNoteNumber - midiNote.noteNumber + 2) + frameWidth + pixelSize / 2
    }

    if (coordinateTopLeft.x < resolution / 2 && resolution / 2 < coordinateBottomRight.x) {
      fill(WHITE)
      coordinateTopLeft.y += pixelSize / 3
      coordinateBottomRight.y += pixelSize / 3
    } else {
      fill(MAIN_COLOR)
    }
    rect(
      coordinateTopLeft.x,
      coordinateTopLeft.y,
      coordinateBottomRight.x,
      coordinateBottomRight.y,
      cornerRadius, cornerRadius, cornerRadius, cornerRadius
    )
  }

  // saveCanvas(`${frameCount}`, "png")
}

